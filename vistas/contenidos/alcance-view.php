                      <h1 class="text">ALCANCE</h1>
                      Con el fin de crear herramientas más eficientes para el manejo y manipulación de datos en el área de la
                      salud , tenemos proyectado elaborar una página web que facilite la gestión de los formularios de
                      afiliación de una Eps , para procesarlos y guardar los datos de los afiliados en una aplicación sencilla,
                      practica y minimalista.
                      Esta aplicación web, garantizara una mejor gestión debido a que se mejora el rendimiento de la parte
                      administrativa de una EPS en procesos tales como organización de datos, manipulación de registros,
                      grabación de formularios, ubicación y búsqueda inmediata de usuarios (Se creara una consulta básica de
                      afilados).
                      Con esta herramienta (software) se le facilitara el trabajo al personal de la administración de dichas
                      EPS, porque se disminuye el gasto en tiempo al momento de organizar y buscar cualquier registro.
                      Nuestro software Cliente-Servidor, Al cual le llamaremos Rapanet Systems, gozara de una interfaz gráfica
                      agradable y un aspecto sencillo e innovador, para impactar en potenciales clientes del mismo; además de
                      esto permitirá un manejo de la información de forma consistente, logrando evitar pérdidas de tiempo en
                      procesos manuales y seguridad en la información grabada.
